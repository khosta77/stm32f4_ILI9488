void lcd7735_sendCmd(uint8_t cmd) {  //uint8_t       //unsigned char
   LCD_DC0; //Set DC low
   lcd7735_senddata(cmd);
}


void lcd7735_sendData(uint8_t data) {  //uint8_t      //unsigned char
   LCD_DC1;//Set DC HIGH
   lcd7735_senddata(data);
}

